'''
Docker containers
    Containers are isolated
        They do not interact with your local filesystem/OS 
       or other containers (unless we configure them to do so)
    Containers are ephemeral
        They can be deleted (& recreated) easily without worry
    Containers are lightweight
        They are single-purpose and contain only what they 
        need for that purpose
    Containers are reproducible
        We can rebuild the same container over & over with a 
        single command
'''